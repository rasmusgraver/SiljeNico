        <h3>From the airport to Kjerringøy:</h3>
        <ol>
            <li> Rent a car and drive to Kjerringøy.
                You can follow this map. (Må få inn en aktiv link på ordet this)
                After approximately 35 min you have to take the ferry. You by your ferry tickets on board. After the ferry you drive for approximately 20 min. Take the road to the left just after the church  (on your right hand side).
            </li><li> Take the bus
                You have to get to the bus station down town. This is a 30 min walk, or 5 min taxi, or take the bus. (Leaves aproximately every 15 min)
                - From the bus station take the <a href="http://www.177nordland.no/?ac_id=1&ac_parent=1" >bus #400.</a>

            </li><li> If non of these solutions suits your travel times, please let us know
            </li>
