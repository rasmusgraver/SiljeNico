
<h2>Overnatting</h2>

<p>
    Nå som svarene fra invitasjonene kommer inn så vil vi etterhvert til å sette opp et forslag til hvem som kan dele leiligheter og sjøboder. Oversikten over dette publiserer vi på denne siden.
</p><p>
    Vi anbefaler Kjerringøy Brygge som overnattingssted og kommer til å booke alle våre gjester inn der om ikke annet blir etterspurt. Det er et helt nytt rorbukompleks og hotell ved havgapet som ble ferdigstillt i 2007. Alle rom, være seg leilighetsrom eller sjøhus så bor det flere sammen, fra 4 til 6 personer.
</p><p>
    Etterhvert som vi mottar bekreftelser så kommer vi til å lage et forslag til hvem som kan bo sammen. Det er naturligvis opp til hver enkelt å bestemme om det er ok. Sjøhusene er privat eid og dette er årsaken til at vi må få landet dette med overnatting så raskt som mulig. Bookingen er en avtale mellom hotell og eier og jo tidligere vi gjør det jo tidligere er vi sikre på å få plass til alle på dette stedet.
</p>

<h3>Hotellrom/leilighet 4 pers</h3>
<img src="images/overn1.jpg" alt="" />
<p>
    Hotellrom er et understatement, siden "rommet" er laget som en leilighet. Det er en dobbeltseng og to enkelt senger som kan slås sammen til en dobbeltseng. Rommet har ikke selvbetjening, men et eget rom med en sofagruppe og Nord-Norges vakreste utsikt.
</p>

<h3>Sjøhus 6 pers</h3>
<img src="images/overn2.jpg" alt="" />
<p>
    Sjøhusene er alle forskjellig innredet, av den enkle grunn at de har ulike eiere. Flotte er de alle sammen og med en helt fantastisk utsikt ut mot Lofoten. Sjøhuset inneholder tre soverom og ett eller to bad.
</p><p>
    For mer informasjon, se:
    <a href="http://www.kjerringoybrygge.no/" > www.kjerringoybrygge.no </a>
</p>


