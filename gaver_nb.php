<p>Siden bryllupet finner sted i nord-norge og vi holder til i Oslo, er det vanskelig for oss å motta gavene "fysisk" i bryllupet.
Vi må derfor vennligst be om at dere ber Illum holde på gavene, og så vil vi selv hente når vi kommer tilbake til Oslo etter bryllupet. </p>
<p>Dersom dere ikke handler på Illums ber vi dere vennligst vurdere om gaven er av et såpass "omfang" at det er enklest å motta den
i Oslo. </p>

